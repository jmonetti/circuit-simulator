#include "Mutex.h"

/*----------------------------------------------------------------------------*/

Mutex::Mutex(){

	// inicializo el mutex
	pthread_mutex_init(&this->mutex,NULL);
}

/*----------------------------------------------------------------------------*/

Mutex::~Mutex(){

	// libero los recursos q utiliza el mutex
	pthread_mutex_destroy(&this->mutex);

}
/*----------------------------------------------------------------------------*/

void Mutex::Lock(){

	pthread_mutex_lock(&this->mutex);
}
/*----------------------------------------------------------------------------*/

void Mutex::unLock(){

	pthread_mutex_unlock(&this->mutex);
}
